results = []
files = sorted(os.listdir(CHARTS_DIR))

for f in files:
    if not f.endswith('.md') or f.startswith('._'):
        continue
    name = f.replace('.md', '')
    content = open(os.path.join(CHARTS_DIR, f), encoding='utf-8').read()
    fm_match = re.search(r'^---\n(.*?)\n---', content, re.DOTALL)
    if not fm_match:
        continue
    try:
        fm = yaml.safe_load(fm_match.group(1))
    except:
        continue
    
    cname = fm.get('name', name)
    aliases = fm.get('aliases', [])
    alias = aliases[0] if aliases and aliases[0] else ''
    faction = fm.get('faction', '群')
    if isinstance(faction, list):
        faction = faction[0] if faction else '群'
    faction = FACTION_MAP.get(faction, faction)
    fbirth = fm.get('birth_year', 0)
    fdeath = fm.get('death_year', 0)
    ftags = fm.get('tags', [])
    
    # 描述取正文前40字
    body = content[fm_match.end():].strip()
    desc = re.sub(r'\n+', ' ', body)[:80].strip()
    # 去掉开头的#
    desc = re.sub(r'^#+\s*', '', desc)
    # 提取第一句
    dot = desc.find('。')
    if dot > 0:
        desc = desc[:dot+1]
    if len(desc) > 60:
        desc = desc[:60] + '…'
    
    hp = HP_MAP.get(cname, 4)
    skills = SKILL_MAP.get(cname, [])
    mastery = MASTERY_MAP.get(cname, [5,5,5])
    
    results.append({
        'id': cname,
        'name': cname,
        'alias': alias,
        'faction': faction,
        'birth': fbirth,
        'death': fdeath,
        'tags': ftags if isinstance(ftags, list) else [ftags] if ftags else [],
        'desc': desc,
        'hp': hp,
        'skills': skills,
        'mastery': {'strength': mastery[0], 'intelligence': mastery[1], 'political': mastery[2]},
        'obsidian': f'characters/{cname}'
    })

# 输出JS
lines = ['// 三国人物数据 - 从sanguo-knowledge自动生成', 'const SANGUO_CHARACTERS = [']
for r in results:
    lines.append('  {')
    for k, v in r.items():
        if k == 'mastery':
            lines.append(f'    mastery: {{ strength: {v["strength"]}, intelligence: {v["intelligence"]}, political: {v["political"]} }},')
        elif k == 'tags':
            tags_str = ', '.join(f"'{t}'" for t in v)
            lines.append(f'    tags: [{tags_str}],')
        elif k == 'skills':
            skills_str = ', '.join(f"'{s}'" for s in v)
            lines.append(f'    skills: [{skills_str}],')
        elif k == 'alias':
            lines.append(f"    alias: '{v}',")
        elif k == 'desc':
            desc_escaped = v.replace("'", "\\'").replace('"', "'")
            lines.append(f"    desc: '{desc_escaped}',")
        elif k == 'obsidian':
            lines.append(f"    obsidian: '{v}'")
        else:
            if isinstance(v, str):
                lines.append(f"    {k}: '{v}',")
            else:
                lines.append(f"    {k}: {v},")
    lines.append('  },')
lines.append('];')

js_content = '\n'.join(lines)
open(OUTPUT_JS, 'w').write(js_content)
print(f"✅ 生成完成: {OUTPUT_JS}")
